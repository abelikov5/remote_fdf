#include "fdf.h"

double	ft_abs(double a)
{
	if (a < 0)
		return (a * (-1));
	return (a);
}

void	swap(int *a, int *b)
{
	int	tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}
